/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
    /** Received byte event **/
   if(LL_USART_IsActiveFlag_RXNE(USART1))
   {
       LL_USART_ClearFlag_RXNE(USART1);
       if(uart1.count < BUFFER_SIZE - 1) {
           uart1.rxBuffer[uart1.count] = LL_USART_ReceiveData8(USART1);
           uart1.count++;

       }
       LL_USART_EnableIT_IDLE(USART1);
   }
    /** Idle event **/
   if(LL_USART_IsActiveFlag_IDLE(USART1))
   {
       LL_USART_DisableIT_IDLE(USART1);
       uart1.flag = true;
       uart1.messageSize = uart1.count;
       uart1.count = 0;
   }
}

/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
    /** Received byte event **/
    if(LL_USART_IsActiveFlag_RXNE(USART2))
    {
        LL_USART_ClearFlag_RXNE(USART2);
        if(uart2.count < BUFFER_SIZE - 1)
        {
            uart2.rxBuffer[uart2.count] = LL_USART_ReceiveData8(USART2);
            uart2.count ++;
        }
        LL_USART_EnableIT_IDLE(USART2);
    }
    /** Idle event **/
    if(LL_USART_IsActiveFlag_IDLE(USART2))
    {
        LL_USART_DisableIT_IDLE(USART2);
        uart2.flag = true;
        uart2.messageSize = uart2.count;
        uart2.count = 0;
    }
}

/**
 * @brief
 * @param USARTx
 * @param rxBuffer
 * @param size
 */
void UART_Send(USART_TypeDef *USARTx,const uint8_t *rxBuffer,uint8_t size){
    uint32_t buffer[size];
    memcpy(buffer,rxBuffer,size);

    for(uint32_t i=0; i<size;i++)
    {
        LL_USART_TransmitData8(USARTx,buffer[i]);
        while(!LL_USART_IsActiveFlag_TXE(USARTx));
    }
}

